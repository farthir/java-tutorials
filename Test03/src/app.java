/*
 * Create a program that creates an array of five hard-coded floating-point values, then prints out just the second value. 
 */

public class app {
	public static void main(String[] args) {
		final float[] NUMBERS = { 1.5f, 6.0f, 10.2f, 3.6f, 2.9f };

		System.out.println("The second floating point value in the array is " + NUMBERS[1]);

	}
}
